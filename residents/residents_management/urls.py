from django.contrib import admin
from django.urls import path, include
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi
from .views import AllDataListCreateView, AllDataRetrieveUpdateDestroyView

# Schema view for Swagger and Redoc
schema_view = get_schema_view(
    openapi.Info(
        title="All Data API",
        default_version='v1',
        description="API for managing buildings, rooms, and residents",
        terms_of_service="https://www.google.com/policies/terms/",
        contact=openapi.Contact(email="contact@yourapi.local"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('accounts/', include('allauth.urls')),  # Include allauth URLs

    path('data/', AllDataListCreateView.as_view(), name='all-data-list-create'),
    path('data/<int:pk>/', AllDataRetrieveUpdateDestroyView.as_view(), name='all-data-retrieve-update-destroy'),

    # API Documentation URLs
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
]
